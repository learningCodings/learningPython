# For Loop

for i in [0, 2, 4, 7, 8, 3]:
    print(i)

# If I want print certain range of number in sequence then I can use range built in function

for i in range(10):
    print(f"This Is Using Built In Range: {i}")

# For String

names = ["Tony", "Thor", "Loki"]

for name in names:
    print(name)

# For loop also can print letter by letter

name = "Scarlet Which"

for character in name:
    print(character)

# Range 

for i in range(101):
    print(f"I Love You...{i} ")
    